/*
   API ? What is that ?

   Acronyme : API = Application Programming Interface - Inteface de programmation des applications

   Voir les schémas suivants :api-rest-architecture.png et api-schema.png

   On va utilise les API comme interface pour recevoir des données d'un système de base de données

   Pour se faire on va poser des questions à cette API par le biai de mots clés tels que : GET / POST / PUT / DELETE au travers des requêtes HTTP et ainsi effectuer des actions (exemple pour le frontend on va beacoup utiliser le GET, qui signifie : donne-moi)

   Au retour les données reviennent au format XML ou Json (ressemble fortement à la structure des objets) 

   JSON = Javascript Object Notation
   XML = eXtended Markup Language

   Json est un format de fichier permettant de stocker facilement des données.
   Le style d'acriture est semblable aux objets Javascript voir fichier : 
   api-objet-json.png et api-objet-json-view.png
   Si ce n'est que les propriétés sont entre guillemets, ce qui n'est pas le cas des objets Javascript.

   Le format Json remplace de plus en plus l'ancien format d'échange XML.

*/