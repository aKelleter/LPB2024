console.log('START OF BOUCLES ___________________________');

// La boucle for()
for(let i = 0 ; i < 5 ; i++)
{
    console.log(i);
}

// La boucle while
let j = 0;
while(j < 5)
{
    console.log(`J est égale à ${j}`);
    j++;
}

// La boucle do while
let k = 10;
do{
    console.log(`K est égale à ${k}`);
    k++;
}while(k < 10);

console.log('END OF BOUCLES _____________________________');
