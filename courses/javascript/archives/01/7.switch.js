let color = 'red';

switch (color)
{
    case 'red':
        console.log("Rouge");
        break;
    case 'green':
        console.log("Vert");
        break;
    case 'blue':
        console.log("Bleu");
        break;
    default:
        console.log("Autre couleur");
        break;
}


